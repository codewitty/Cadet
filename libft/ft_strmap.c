char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*p;
	int		i;

	while (s[i])
	{
		i++;
	}
	p = (char *)malloc(sizeof(char) * (i + 1));
	while (s[i])
	{
		p[i] = f(&s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
