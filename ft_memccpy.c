#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t len)
{
	size_t		i;
	char		*src2;
	char		*dst2;

	dst2 = (char *)dst;
	src2 = (char *)src;
	i = 0;
	while (i < len)
	{
		dst2[i] = src2[i];
		if ((unsigned char)src2[i] == (unsigned char)c)
			return ((char *)dst + i + 1);
		i++;
	}
	return (NULL);
}
